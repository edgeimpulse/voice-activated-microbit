const unsigned char* BitmapFont::defaultFont = pendolino3;
BitmapFont BitmapFont::systemFont = BitmapFont(defaultFont, BITMAP_FONT_ASCII_END);

/**
  * Constructor.
  *
  * Sets the font represented by this font object.
  *
  * @param font A pointer to the beginning of the new font.
  *
  * @param asciiEnd the char value at which this font finishes.
  */
BitmapFont::BitmapFont(const unsigned char* characters, int asciiEnd)
{
    this->characters = characters;
    this->asciiEnd = asciiEnd;
}

/**
  * Default Constructor.
  *
  * Configures the default font for the display to use.
  */
BitmapFont::BitmapFont()
{
    this->characters = defaultFont;
    this->asciiEnd = BITMAP_FONT_ASCII_END;
}

/**
  * Modifies the current system font to the given instance of BitmapFont.
  *
  * @param font the new font that will be used to render characters on the display.
  */
void BitmapFont::setSystemFont(BitmapFont font)
{
	BitmapFont::systemFont = font;
}

/**
  * Retreives the font object used for rendering characters on the display.
  */
BitmapFont BitmapFont::getSystemFont()
{
    return BitmapFont::systemFont;
}

/**
 * Returns a pointer to the start of the memory buffer for the given ASCII character.
 *
 * @param c The character to look up
 * @return A pointer to the corresponding data buffer, or NULL if the character is not available.
 */
const uint8_t* BitmapFont::get(char c)
{
    if (c < BITMAP_FONT_ASCII_START || c > BITMAP_FONT_ASCII_END)
        return NULL;

    return characters + (c-BITMAP_FONT_ASCII_START) * ((1 + (BITMAP_FONT_WIDTH / 8)) * BITMAP_FONT_HEIGHT);
}
